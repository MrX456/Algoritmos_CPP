/*
Um automóvel é algo que nos permite movimentamos de maneira mais rápida de um ponto a outro. Dentre os principais atributos de um automóvel podemos citar:
rodas, portas, assentos, janelas, volante, freios, motor, escapamento, transmissão, suspensão, faróis, lanternas, pneus, etc.

Transformando estes conceitos em objetos:

Rodas
Atributos : tamanho, cor, fabricante
Comportamentos : girar para frente, girar para tras, parar de girar

Motor
Atributos: cilindros, potencia, torque, combustivel
Comportamentos: ligar, desligar, acelerar.

Portas
Atributos: quantidade, aberta, travada
Comportamentos: abrir, fechar, travar, destravar

Janelas
Atributos: quatidade, cor, transparencia, aberta
Comportamentos: abrir, fechar

Freios
Atributos: pressionado, tipo, abs
Comportamentos:  pressionar, travar

As interações são:
Motorista vira o volante na direção desejada e rodas acompanham
Motorista acelera e o motor trabalha mais rapido dando mais velocidade ao automóvel, aumentando velocidade das rodas e travando as portas
Motorista abre ou fecha porta
Motorista pisa no pedal do freio diminuindo a velocidade do automóvel ou parando completamente, parando as rodas
Motorista pode levantar ou abaixar o vidro do automóvel
*/
